func TestNodeModulesAdapter_SingleMatch(t *testing.T) {
	home := t.TempDir()
	t.Setenv("HOME", home)
	nm := filepath.Join(home, "projects", "my-project", "node_modules")
	os.MkdirAll(filepath.Join(nm, "some-pkg"), 0755)
	os.WriteFile(filepath.Join(nm, "some-pkg", "index.js"), []byte("// test"), 0644)

	a := &NodeModulesAdapter{}
	results, err := a.Scan(context.Background())
	if err != nil {
		t.Fatal(err)
	}
	if len(results) != 1 {
		t.Fatalf("expected 1, got %d", len(results))
	}
	if results[0].ID != "my-project" {
		t.Errorf("ID = %q; want 'my-project' (parent dir name)", results[0].ID)
	}
	if results[0].Tool != types.ToolNodeModules {
		t.Errorf("Tool = %q; want %q", results[0].Tool, types.ToolNodeModules)
	}
	if results[0].Size <= 0 {
		t.Errorf("Size = %d; want > 0", results[0].Size)
	}
	if results[0].ModTime.IsZero() {
		t.Error("ModTime is zero")
	}
}

func TestNodeModulesAdapter_MultipleMatches(t *testing.T) {
	home := t.TempDir()
	t.Setenv("HOME", home)
	nm1 := filepath.Join(home, "projects", "proj1", "node_modules")
	os.MkdirAll(filepath.Join(nm1, "pkg1"), 0755)
	nm2 := filepath.Join(home, "projects", "proj2", "node_modules")
	os.MkdirAll(filepath.Join(nm2, "pkg2"), 0755)

	a := &NodeModulesAdapter{}
	results, err := a.Scan(context.Background())
	if err != nil {
		t.Fatal(err)
	}
	if len(results) != 2 {
		t.Fatalf("expected 2, got %d", len(results))
	}
	ids := map[string]bool{}
	for _, r := range results {
		ids[r.ID] = true
	}
	if !ids["proj1"] || !ids["proj2"] {
		t.Errorf("missing expected IDs: %v", results)
	}
}

