func TestNodeModulesAdapter_ContextCancellation(t *testing.T) {
	home := t.TempDir()
	t.Setenv("HOME", home)
	os.MkdirAll(filepath.Join(home, "projects", "my-project", "node_modules", "pkg"), 0755)

	ctx, cancel := context.WithCancel(context.Background())
	cancel()

	a := &NodeModulesAdapter{}
	_, err := a.Scan(ctx)
	if err == nil {
		t.Fatal("expected error, got nil")
	}
	if !errors.Is(err, context.Canceled) {
		t.Errorf("expected Canceled, got %v", err)
	}
}
