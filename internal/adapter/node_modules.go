package adapter

import (
	"context"
	"os"
	"path/filepath"

	"github.com/sungjunlee/aibris/internal/types"
)

type NodeModulesAdapter struct{}

func (a *NodeModulesAdapter) Name() types.Tool {
	return types.ToolNodeModules
}

func (a *NodeModulesAdapter) Category() types.Category {
	return types.CategoryNodeModules
}

func (a *NodeModulesAdapter) Scan(ctx context.Context, opts types.ScanOptions) ([]types.DebrisInfo, error) {
	select {
	case <-ctx.Done():
		return nil, ctx.Err()
	default:
	}

	roots, err := scanRootsOrHome(opts.Roots)
	if err != nil {
		return nil, err
	}

	var results []types.DebrisInfo
	for _, root := range roots {
		walkErr := filepath.WalkDir(root, func(path string, d os.DirEntry, err error) error {
			if err := ctx.Err(); err != nil {
				return err
			}
			if err != nil {
				return filepath.SkipDir
			}
			if !d.IsDir() {
				return nil
			}
			if d.Name() == "node_modules" {
				info, err := d.Info()
				if err != nil {
					return filepath.SkipDir
				}
				results = append(results, types.DebrisInfo{
					Tool:     types.ToolNodeModules,
					Category: types.CategoryNodeModules,
					ID:       filepath.Base(filepath.Dir(path)),
					Path:     path,
					ModTime:  info.ModTime(),
				})
				return filepath.SkipDir
			}
			if path != root && shouldSkipNodeModulesDir(d.Name()) {
				return filepath.SkipDir
			}
			return nil
		})
		if walkErr != nil {
			return nil, walkErr
		}
	}
	sizePaths := make([]string, 0, len(results))
	for _, result := range results {
		sizePaths = append(sizePaths, result.Path)
	}
	sizes := estimateDirSizes(ctx, sizePaths)
	for i := range results {
		results[i].Size = sizes[results[i].Path]
	}
	return results, nil
}

func shouldSkipNodeModulesDir(name string) bool {
	switch name {
	case ".Trash", "Library", "Applications", "Pictures", "Movies", "Music", ".git", "vendor":
		return true
	default:
		return isHiddenDir(name)
	}
}
