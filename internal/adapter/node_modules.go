package adapter

import (
	"context"
	"os"
	"path/filepath"

	"github.com/sungjunlee/aibris/internal/types"
)

type NodeModulesAdapter struct{}

func (a *NodeModulesAdapter) Name() types.Tool {
	return types.ToolNodeModules
}

func (a *NodeModulesAdapter) Category() types.Category {
	return types.CategoryNodeModules
}

func (a *NodeModulesAdapter) Scan(ctx context.Context) ([]types.WorktreeInfo, error) {
	select {
	case <-ctx.Done():
		return nil, ctx.Err()
	default:
	}

	home, err := os.UserHomeDir()
	if err != nil {
		return nil, err
	}

	base := filepath.Join(home, "projects")
	info, err := os.Stat(base)
	if err != nil {
		if os.IsNotExist(err) {
			return nil, nil
		}
		return nil, err
	}
	if !info.IsDir() {
		return nil, nil
	}

	var results []types.WorktreeInfo
	walkErr := filepath.WalkDir(base, func(path string, d os.DirEntry, err error) error {
		if err := ctx.Err(); err != nil {
			return err
		}
		if err != nil {
			return filepath.SkipDir
		}
		if d.IsDir() && d.Name() == "node_modules" {
			info, err := d.Info()
			if err != nil {
				return filepath.SkipDir
			}
			results = append(results, types.WorktreeInfo{
				Tool:     types.ToolNodeModules,
				Category: types.CategoryNodeModules,
				ID:       filepath.Base(filepath.Dir(path)),
				Path:     path,
				Size:     estimateDirSize(ctx, path),
				ModTime:  info.ModTime(),
			})
			return filepath.SkipDir
		}
		if d.IsDir() && isHiddenDir(d.Name()) {
			return filepath.SkipDir
		}
		return nil
	})
	if walkErr != nil {
		return nil, walkErr
	}
	return results, nil
}
